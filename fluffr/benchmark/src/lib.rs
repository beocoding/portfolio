// ── Tracking allocator ────────────────────────────────────────────────────────
use std::alloc::{GlobalAlloc, Layout, System};
use std::sync::atomic::{AtomicUsize, Ordering::SeqCst};

pub struct TrackingAllocator;

static LIVE: AtomicUsize = AtomicUsize::new(0);
static PEAK: AtomicUsize = AtomicUsize::new(0);

#[global_allocator]
pub static ALLOC: TrackingAllocator = TrackingAllocator;

impl TrackingAllocator {
    pub fn reset(&self)         { LIVE.store(0, SeqCst); PEAK.store(0, SeqCst); }
    pub fn peak_bytes(&self) -> usize { PEAK.load(SeqCst) }
}

unsafe impl GlobalAlloc for TrackingAllocator {
    unsafe fn alloc(&self, layout: Layout) -> *mut u8 {
        let ptr = System.alloc(layout);
        if !ptr.is_null() {
            let prev    = LIVE.fetch_add(layout.size(), SeqCst);
            let new_live = prev + layout.size();
            let mut peak = PEAK.load(SeqCst);
            while new_live > peak {
                match PEAK.compare_exchange_weak(peak, new_live, SeqCst, SeqCst) {
                    Ok(_)  => break,
                    Err(p) => peak = p,
                }
            }
        }
        ptr
    }

    unsafe fn dealloc(&self, ptr: *mut u8, layout: Layout) {
        System.dealloc(ptr, layout);
        LIVE.fetch_sub(layout.size(), SeqCst);
    }
}


pub fn make_fbs_product_bytes(i: usize) -> Vec<u8> {
    let mut b = FlatBufferBuilder::with_capacity(512);

    let sku         = b.create_string(&format!("SKU-{:06}", i));
    let label       = b.create_string(&format!("Product Label {}", i));
    let slug        = b.create_string(&format!("product-label-{}", i));
    let description = b.create_string(&format!("Description for product {}", i));
    let brand_name  = b.create_string(&format!("Brand {}", i % 5));
    let tag0        = b.create_string(&format!("tag-{}", i % 3));
    let tag1        = b.create_string("common");
    let tags        = b.create_vector(&[tag0, tag1]);

    let brand = FbsBrand::create(&mut b, &FbsBrandArgs { name: Some(brand_name) });

    let dims = FbsDimensions::new(10.0 + i as f32, 5.0, 2.0);

    let link_type;
    let link: flatbuffers::WIPOffset<flatbuffers::UnionWIPOffset>;
    match i % 3 {  // None variant has no table so skip it
        0 => {
            let sku_str = b.create_string(&format!("REF-{}", i));
            let l = FbsLinkBySku::create(&mut b, &FbsLinkBySkuArgs { sku: Some(sku_str) });
            link_type = FbsProductLink::LinkBySku;
            link = l.as_union_value();
        }
        1 => {
            let l = FbsLinkByDims::create(&mut b, &FbsLinkByDimsArgs {
                dims: Some(&FbsDimensions::new(i as f32, 1.0, 1.0))
            });
            link_type = FbsProductLink::LinkByDims;
            link = l.as_union_value();
        }
        _ => {
            let brand_name2 = b.create_string(&format!("Linked {}", i));
            let l = FbsBrand::create(&mut b, &FbsBrandArgs { name: Some(brand_name2) });
            link_type = FbsProductLink::Brand;
            link = l.as_union_value();
        }
    }

    let product = FbsProduct::create(&mut b, &FbsProductArgs {
        sku:         Some(sku),
        label:       Some(label),
        slug:        Some(slug),
        description: Some(description),
        brand:       Some(brand),
        tags:        Some(tags),
        category:    fbs_category(i),
        price:       9.99 + i as f32,
        weight:      0.5 + i as f32 * 0.1,
        dimensions:  Some(&dims),
        link_type,
        link:        Some(link),
    });

    b.finish(product, None);
    b.finished_data().to_vec()
}

fn fbs_category(i: usize) -> FbsCategory {
    match i % 5 {
        0 => FbsCategory::Electronics,
        1 => FbsCategory::Apparel,
        2 => FbsCategory::Home,
        3 => FbsCategory::Grocery,
        _ => FbsCategory::Other,
    }
}