#[derive(Table, Clone, Default)]
pub struct FlatrEvent {
    pub r#type:   String,
    pub subject:  String,
    pub source:   String,
    pub time:     String,
    pub data:     String,
}

impl JsonEvent {
    pub fn sample(data_len: usize) -> Self {
        Self {
            r#type:   "button.clicked".to_string(),
            subject:  "user1000".to_string(),
            source:   "service-b".to_string(),
            time:     "2018-04-05T17:31:00Z".to_string(),
            data:     "a".repeat(data_len),
        }
    }
}

impl From<&JsonEvent> for ProtoEvent {
    fn from(e: &JsonEvent) -> Self {
        Self {
            r#type:   e.r#type.clone(),
            subject:  e.subject.clone(),
            source:   e.source.clone(),
            time:     e.time.clone(),
            data:     e.data.clone(),
        }
    }
}

impl FlatrEvent {
    pub fn sample(data_len: usize) -> Self {
        let j = JsonEvent::sample(data_len);
        Self { r#type: j.r#type, subject: j.subject,
               source: j.source, time: j.time, data: j.data }
    }
}

